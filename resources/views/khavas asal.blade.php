    <h1>خواص عسل را چقدر میشناسید ؟</h1>

    <p>
        با استفاده از این ثروت باد اورده از خواص عسل برای هزاران سال است که عسل بعنوان یک درمان دارویی مورد استفاده قرار میگیرد. اولین استفاده ثبت شده از عسل به عنوان یک تغذیه درمانی بوده است که از لوح های گلی سومری یافت شده است  .همچنین مصریان باستان از عسل به طور منظم برای درمان مشکلات پوستی و چشم استفاده میکرده اند .

        برای هزاران سال، عسل به عنوان یکی از مهمترین و شناخته شده ترین داروی خانگی طبیعی برای درمان طیف گسترده ای از بیماری ها و مشکلات از جمله عفونت قارچی، مشکلات پای ورزشکاران و درد مفاصل بوده است.

        با این حال، باید توجه داشت که بسیاری از ادعاهای که درباره خواص عسل گفته میشود با اینکه تحقیقات زیادی انجام شده و ما سعی کرده ایم از معتبرترین منابع استفاده کنیم اما هنوز هم نیاز به مطالعات علمی دقیق دارد و بسیاری از خواص عسل همچنان ناشناخته است .

        عسل یک درمان سنتی محبوب است، همچنین در سیستم های شفای سنتی از قبیل آیورودا و طب سنتی چین توصیه می شود. طبق مطالعات انجام شده، محتویات این اکسیر کهربایی رنگ دارای خواصی می باشد که ذکر خواهیم کرد:  قلیایی کننده، ضد سرطان، ضد میکروب، ضد چاقی، آنتی اکسیدان، ضد التهاب، تقویت کننده مویرگ ها، کاهش دهنده کلسترول، سم زدا، بهبود دهنده گوارش، تقویت کننده دستگاه ایمنی، انتقال دهنده ی پیام های عصبی و تنش زدایی از عضلات صاف.

        میزان اهمیت هر کدام از این اثرات در عسل های مختلف، متفاوت است. عسلی که منشا آن گیاهان دارویی است، می تواند حاوی برخی ترکیبات موجود درد اروهای گیاهی ساخته شده از ریشه، ساقه، برگ، گل، دانه یا میوه ی آن ها باشد، بنابراین شاید ما باید این عسل ها را هم به عنوان درمان های گیاهی تلقی کنیم.
