    <h2>۹- حل و فصل مشکلات پوست سر و شوره سر</h2>

    <p> در یک مطالعه شامل بیماران مبتلا به درماتیت سبورئیک مزمن و شوره سر ، از شرکت کنندگان خواسته شد تا عسل رقیق شده با ۱۰ درصد آب گرم را بر روی پوست و موی سر خود قرار دهند و پس از مدت سه ساعت آن را شست و شو دهند . در تمام بیماران، خارش و پوسته پوسته شدن پوست سر در عرض یک هفته ناپدید شد همچنین ضایعات پوستی به طور کامل در عرض دو هفته بهبود یافت .

        عسل رقیق شده با کمی آب گرم به طور قابل توجهی در بهبود درماتیت سبورئیک که یک بیماری پوست سر است موثر است.</p>

    <h2>۱۰- عسل و دیابت</h2>
    <p>
        عسل در زمینه پیشگیری دیابت و درمان زخم های دیابتی بسیار موثر هست. عسل انسولین را افزایش می دهد و قند خون را کاهش میدهد.تحقیقات اخیر نشان داده است که جایگزینی کامل شکر با عسل خطر ابتلا به دیابت را تا ۷۰ درصد کاهش می دهد و همینطور با مالیدن عسل بر روی زخم های دیابتی می توان بهبودی این زخم ها را سرعت بخشید و حتی از لزوم قطع شدن پای دیابتی جلوگیری کرد. به منظور پیشگیری از ابتلا به دیابت خانواده ها باید استفاده از عسل را  به جای شکر در برنامه غذایی خودشان جدی بگیرند، اینگونه هم از ابتلا به دیابت جلوگیری میکنند و هم اینکه از خواص عسل بیشترین استفاده را میبرند.
    </p>

