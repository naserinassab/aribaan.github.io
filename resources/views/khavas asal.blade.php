    <h1>۳۱ مورد از بهترین خواص عسل :</h1>
    <h2>1-عسل ضد سرطان است.</h2>
    <p>
        میدانستید که به کمک عسل میتوانید با سرطان مبارزه کنید؟ چه بسیاری از مردم که نمیدانند عسل دارای خواص سرطان زا و ضد تومور است! مطالعات اکنون بیشتر و بیشتر به نقش بالقوه عسل در پیشگیری و پیشرفت تومور و سرطان اشاره دارد. عسل حاوی فلاونوئید و آنتی اکسیدان ها است که به کاهش خطر ابتلا به برخی سرطان ها و بیماری های قلبی کمک میکند .
    </p>
    <h2>- عسل قویترین آنتی بیوتیک طبیعی</h2>
    <p>
        یکی از قویترین خواص عسل خاصیت آنتی بیوتیکی آن است . طبق تحقیقات بعمل آمده عسل از رشد باکتریهایی مثل استافیلکوک اورئوس و میکروب های مهاجم به دستگاه گوارش و قارچ هایی نظیر کاندیدا آلبیکانس جلوگیری می کند . هر چند هر عسل دارای رنگ خاصی میباشد ولی معمولا عسل هایی که تیره تر هستند دارای خاصیت آنتی بیوتیکی و آنتی‌اکسیدانی قوی تری هستند .
        عسل میتواند به طور موثر در از بین بردن بیش از ۲۵۰ سویه های کلینیکی از باکتری ها موثر باشد، از جمله گونه های مقاوم مانند :

        MRSA (استافیلوکوکوس اورئوس مقاوم به متی سیلین)
        MSSA (استافیلوکوکوس اورئوس حساس به متی سیلین)
        VRE (مقاوم در برابر وانکومایسین انتروکوک)

    </p>

    <h2>- درمان زخم و سوختگی با عسل</h2>
    <p>
        این قدرت شفابخش اغراق آمیز نیست ، خواص عسل بیش از آن هستند که فکر میکنید. عسل یک آنتی بیوتیک طبیعی است که می تواند هم در داخل و هم در خارج بدن بصورت مفید عمل کند. می توان آن را به عنوان یک درمان مرسوم برای زخم و سوختگی و ضد عفونی زخمها استفاده کرد..

        عسل درمان متداول در مبارزه با عفونت تا اوایل قرن ۲۰ بوده است که در آن زمان استفاده از آن به آرامی با ظهور پنی سیلین از بین رفت . در حال حاضر استفاده از عسل در مراقبت از زخم در حال بدست آوردن محبوبیت است .محققان در حال این تحقیق هستند که  دقیقا چگونه عسل می تواند در مبارزه با عفونت های جدی پوست کمک کند.

        عسل دارای فعالیت ضد باکتری، ضد قارچ، و آنتی اکسیدان است که آن را برای درمان زخم کاملا ایده آل کرده است .

        تحقیقات نشان می دهد که هر نوع عسل فرآوری نشده ای میتواند زخم ها را التیام بخشد. در یک مطالعه،  ۵۸ نوع از ۵۹ زخم با مصرف موضعی عسل ، بهبود بسیار مناسبی پیدا کردند .
    </p>

    <h2>- نوشابه ای انرژی زا </h2>
    <p>یکی دیگر از بهترین خواص عسل ، انرژی زا بودن آن است ، عسل مانند نوشابه ای انرژی زا است که البته کاملا طبیعی است. با توجه به گزارش وزارت کشاورزی ایالات متحده، عسل شامل حدود ۶۴ کالری در هر قاشق غذا خوری میباشد که توسط بسیاری از مردم به عنوان یک منبع انرژی استفاده می شود. این قند فرآوری نشده طبیعی ( گلوکز و فروکتوز ) به طور مستقیم وارد جریان خون شده و می تواند باعث افزایش سریع انرژی شود. افزایش قند خون به عنوان یک منبع انرژی در کوتاه مدت است و برای تمرین ها به خصوص در تمرینات استقامتی بسیار مفید است .</p>

    <h2>- افزایش حافظه</h2>
    <p>مطالعات نشان داده است که عسل دارای آنتی اکسیدان طبیعی است و دارای خواص درمانی است که قادر به جلوگیری از کاهش شناختی و زوال عقل و افزایش سیستم کولینرژیک مغز میباشد .این خاصیت عسل به بدن در جذب کلسیم، کمک می کند تا مغز سالم تر و قوی تر شود . مغز به کلسیم به منظور پردازش اندیشه و تصمیم گیری نیاز دارد .</p>


    <h2>۶- درمان سرماخوردگی و سرفه</h2>
    <p>
        و آکادمی اطفال آمریکا عسل را به عنوان یک درمان طبیعی برای سرفه توصیه کرده است . تحقیقی در سال ۲۰۰۷ توسط کالج پزشکی پن استیت نشان می دهد که عسل باعث کاهش سرفه های شبانه و بهبود کیفیت خواب در کودکان مبتلا به عفونت تنفسی فوقانی میشود و بهتر از داروی دکسترومتورفان برای سرفه عمل میکند . یکی از خواص عسل که باعث سلامتی میشود درمان گلو درد است . خواص ضد میکروبی عسل آن نه تنها گلو را آرام میکند بلکه همچنین می تواند باکتری های خاصی که باعث عفونت میشوند را بکشد
    </p>

    <h2>۷- درمان بیخوابی</h2>
    <p> نمیتوانید به راحتی بخوابید ؟ با استفاده از ترکیب معروف شیر و عسل این مشکل را درمان کنید . یک  قاشق چای خوری عسل را به یک لیوان شیر داغ اضافه کنید تا بسیار سریع به آرامش روح و القاء خواب برسید . همچنین با اضافه کردن ۱ یا ۲ قاشق چای خوری عسل به یک فنجان چای بابونه نیز میتوانید بیخوابی را درمان کنید .

        عسل می تواند با افزایش انسولین و انتشار سروتونین که یک انتقال دهنده عصبی است باعث بهبود خلق و خو و شادی انسان بشود .</p>

    <h2>۸- عسل و آلرژی</h2>
    <p>برخی از تحقیقات نشان می دهد که عسل در به حداقل رساندن آلرژی های فصلی بسیارمفید است . جالب است بدانید تمام گرده‌هایی که زنبور عسل با خود حمل می‌کند، به عسل منتقل می‌شود. بنابراین، وقتی عسل محلی می‌خورید، سیستم ایمنی بدنتان را در معرض یک آلرژن طبیعی قرار می‌دهید. این امر باعث می‌شود که سیستم ایمنی بدن شما به مرور زمان در برابر این آلرژن مقاوم شود.

        خوردن عسل از یک ماه مانده به آغاز فصل گرده افشانی به شما کمک می‌کند که از تب یونجه و آلرژی‌های فصلی در امان باشید. با مصرف یک قاشق غذاخوری عسل پس از هر وعده غذایی شروع کنید. جویدن یک تکه موم کوچک نیز به کاهش بیشتر علائم آلرژی کمک میکند .

        البته تمام این خصوصیاتی که گفتیم درباره ی مصرف عسل طبیعی است . اگر نمیدانید چگونه یک عسل طبیعی را از عسل تقلبی تشخیص دهید پس مقاله زیر را مطالعه کنید .
    </p>
